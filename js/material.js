function formatMat(name) {
    var words = name.split('_');
    var result = '';
    var first = true;
    for (var i = 0; i < words.length; i++) {
        if (!first) result += ' ';
        first = false;
        result += words[i].charAt(0) + words[i].substr(1).toLowerCase();
    }
    return result;
}